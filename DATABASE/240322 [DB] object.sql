-- NUM_ROWS : 행의 개수
-- -> COUNT(*) 함수로 행이 개수를 확인할 때 행의 개수가 많으면 많을수록 시간이 오래 걸림
-- -> NUM_ROWS로 확인하면 빠르다

-- 권한이 있는 테이블만 확인
SELECT * FROM ALL_TABLES;

-- 관리자 권한이 있어야 확인 가능
